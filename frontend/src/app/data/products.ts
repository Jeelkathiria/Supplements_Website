export const calculateFinalPrice = (
  basePrice: number,
  discount: number,
  tax: number,
): number => {
  const discountedPrice =
    basePrice - (basePrice * discount) / 100;
  const finalPrice =
    discountedPrice + (discountedPrice * tax) / 100;
  return Math.round(finalPrice);
};